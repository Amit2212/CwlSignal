## Build instructions

This playground requires the CwlSignal.framework built by the CwlSignal_macOS scheme. To satisfy this requirement:

1. You must not open CwlSignal.playground on its own. Instead, open the CwlSignal.xcodeproj in Xcode and click on the CwlSignal.playground in the file tree.
2. Select the "CwlSignal_macOS" scheme (from the "Product" → "Scheme" menu or the scheme popup menu if it's available in your toolbar) and build.
3. You will usually need to close the playground page and re-open before it will pick up the dependency. Close an already open page by pressing Command-Control-W (or selecting "Close Introduction.xcplaygroundpage" from the File menu) before clicking in the file tree to re-open.

Failure to follow these steps correctly will result in a "Playground execution failed: error: no such module 'CwlSignal'" error (along with a large number of stack frames) logged to the Debug Area (show with the "View" → "Debug Area" → "Show Debug Area" menu if it is not visible.

